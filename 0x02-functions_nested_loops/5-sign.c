#include "main.h"

/**
 * print_sign - prints the sign of a digit
 *
 * Description: printing should be done with _putchar function.
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
